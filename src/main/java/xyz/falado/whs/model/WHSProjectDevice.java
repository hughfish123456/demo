package xyz.falado.whs.model;

import java.io.Serializable;

public class WHSProjectDevice implements Serializable {
    private Integer id;

    private Integer projectId;

    private String deviceCode;

    private Integer deviceQuantity;

    private Integer deviceType;

    private Integer deviceSpecification;

    private Integer deviceOption;

    private static final long serialVersionUID = 1L;

    public WHSProjectDevice(Integer id, Integer projectId, String deviceCode, Integer deviceQuantity, Integer deviceType, Integer deviceSpecification, Integer deviceOption) {
        this.id = id;
        this.projectId = projectId;
        this.deviceCode = deviceCode;
        this.deviceQuantity = deviceQuantity;
        this.deviceType = deviceType;
        this.deviceSpecification = deviceSpecification;
        this.deviceOption = deviceOption;
    }

    public WHSProjectDevice() {
        super();
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getProjectId() {
        return projectId;
    }

    public void setProjectId(Integer projectId) {
        this.projectId = projectId;
    }

    public String getDeviceCode() {
        return deviceCode;
    }

    public void setDeviceCode(String deviceCode) {
        this.deviceCode = deviceCode == null ? null : deviceCode.trim();
    }

    public Integer getDeviceQuantity() {
        return deviceQuantity;
    }

    public void setDeviceQuantity(Integer deviceQuantity) {
        this.deviceQuantity = deviceQuantity;
    }

    public Integer getDeviceType() {
        return deviceType;
    }

    public void setDeviceType(Integer deviceType) {
        this.deviceType = deviceType;
    }

    public Integer getDeviceSpecification() {
        return deviceSpecification;
    }

    public void setDeviceSpecification(Integer deviceSpecification) {
        this.deviceSpecification = deviceSpecification;
    }

    public Integer getDeviceOption() {
        return deviceOption;
    }

    public void setDeviceOption(Integer deviceOption) {
        this.deviceOption = deviceOption;
    }
}